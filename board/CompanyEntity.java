package univ;

public class CompanyEntity {	
     private String cname;
     private String address;
     private String juname;
     private String juje;
     private int money;
     private int cnt;
     private int newcnt;
     
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getJuname() {
		return juname;
	}
	public void setJuname(String juname) {
		this.juname = juname;
	}
	public String getJuje() {
		return juje;
	}
	public void setJuje(String juje) {
		this.juje = juje;
	}
    public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getNewcnt() {
		return newcnt;
	}
	public void setNewcnt(int newcnt) {
		this.newcnt = newcnt;
	}
	
	

     
}
